var d = prompt("Введите число в диапазоне от 0-9")
if(d==0){
    alert("Ноль");
}else if(d==1){
    alert("Один");
}else if(d==2){
    alert("Два");
}else if(d==3){
    alert("Три");
}else if(d==4){
    alert("Четыре");
}else if(d==5){
    alert("Пять");
}else if(d==6){
    alert("Шесть");
}else if(d==7){
    alert("Семь");
}else if(d==8){
    alert("Восемь");
}else if(d==9){
    alert("Девять");
}else{
    alert("Неверное число");
}